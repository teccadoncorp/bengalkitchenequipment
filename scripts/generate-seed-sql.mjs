// One-off migration helper: reads the existing static src/data/products.ts
// and prints SQL INSERT statements to seed a freshly-created Supabase database.
//
// Usage:
//   node scripts/generate-seed-sql.mjs > seed.sql
// Then paste seed.sql into the Supabase SQL Editor and run it once.
//
// This script never touches your Supabase project directly and needs no
// credentials — it only reads a local file and prints SQL text.

import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import { transformSync } from 'esbuild';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const dataFilePath = path.join(__dirname, '..', 'src', 'data', 'products.ts');

const source = readFileSync(dataFilePath, 'utf8');
const { code } = transformSync(source, { loader: 'ts', format: 'cjs' });

const moduleObj = { exports: {} };
new Function('module', 'exports', code)(moduleObj, moduleObj.exports);

const { products, categories } = moduleObj.exports;

if (!Array.isArray(products) || !Array.isArray(categories)) {
  throw new Error('Could not extract products/categories arrays from src/data/products.ts');
}

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlTextArray(values) {
  if (!values.length) return "'{}'::text[]";
  return `ARRAY[${values.map(sqlString).join(', ')}]::text[]`;
}

function sqlJsonb(obj) {
  const json = JSON.stringify(obj ?? {});
  return `${sqlString(json)}::jsonb`;
}

const realCategories = categories.filter((c) => c.id !== 'all');

const lines = [];

lines.push('-- Generated by scripts/generate-seed-sql.mjs — run once in the Supabase SQL Editor.');
lines.push('');
lines.push('-- Categories');
realCategories.forEach((category, index) => {
  lines.push(
    `insert into public.categories (id, name, icon, sort_order) values (${sqlString(
      category.id
    )}, ${sqlString(category.name)}, ${sqlString(category.icon)}, ${index});`
  );
});

lines.push('');
lines.push('-- Products');
products.forEach((product) => {
  lines.push(
    `insert into public.products (id, name, category, description, image, features, specifications) overriding system value values (${
      product.id
    }, ${sqlString(product.name)}, ${sqlString(product.category)}, ${sqlString(
      product.description
    )}, ${sqlString(product.image)}, ${sqlTextArray(product.features)}, ${sqlJsonb(
      product.specifications
    )});`
  );
});

lines.push('');
lines.push('-- Keep future admin-created products from colliding with the seeded ids.');
lines.push(
  "select setval(pg_get_serial_sequence('public.products', 'id'), (select max(id) from public.products));"
);

console.log(lines.join('\n'));
console.error(
  `\nGenerated ${realCategories.length} category rows and ${products.length} product rows.`
);
